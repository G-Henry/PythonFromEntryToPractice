# 3.3.1 sort()
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)
cars.sort(reverse=True)
print(cars)

# 3.3.2 sorted()
print("Here is the original list:")
print(cars)

print("Here is the sorted list:")
print(sorted(cars))

print("Here is the origine list again:")
print(cars)

# 3.3.3 reverse
alist = ['a', 'b', 'd', 'c']
print(alist.reverse())
print(alist)

# 3.3.4 len()
print(len(cars))
